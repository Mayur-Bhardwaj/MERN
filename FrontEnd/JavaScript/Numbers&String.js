// Arithmetic Operator

a=10;
b=5;
console.log(a+b);  //15
console.log(a-b);  //5
console.log(a*b);  //50
console.log(a/b);  //2
console.log(a%b);  //0

no = 2.6;

console.log(Math.round(no));  //3
console.log(Math.ceil(no));  //3
console.log(Math.floor(no)); //2
console.log(Math.trunc(no)); //Remove only the decimal part  //2



