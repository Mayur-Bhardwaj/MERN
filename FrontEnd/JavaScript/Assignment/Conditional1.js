// Conditional Operator:- >, <, >=, <=, =, ==, ===, !=, !==.

let a = 10;
let b = 20;

console.log(a>b); // false
console.log(a<b);  //  true

console.log(a==b, typeof(a), typeof(b));  // false number number

let ans = a!=b;
console.log(ans, typeof(ans));  // true boolean


let x = "10";  // string
let y = 10;  // number
console.log(x==y, typeof(x), typeof(y));  // true string number

let s = "20";  // string
let d = 20;  // number

console.log(s===d, typeof(s), typeof(d));  // false string number




